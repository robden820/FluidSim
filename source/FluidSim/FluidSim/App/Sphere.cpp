void Sphere::SetRadius(float inRadius)
{
	float invCurrentRadius = 1.0f / mRadius;
	mRadius = inRadius;

	for (int v = 0; v < mNumVertices; v++)
	{
		mVertices[v] *= invCurrentRadius;
		mVertices[v] *= mRadius;
	}
}